#!/usr/bin/env node
/**
 * i18n type generation script
 *
 * Reads namespace JSON files and generates TypeScript types.
 * Structure: locales/{lang}/{namespace}.json
 */

import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import { NAMESPACES } from './i18n-config.mjs';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const LOCALES_DIR = path.join(__dirname, '../src/i18n/locales');
const OUTPUT_PATH = path.join(__dirname, '../src/i18n/types.generated.ts');

/**
 * Convert a prefix to PascalCase
 */
function toPascalCase(str) {
  return str
    .split(/[-_]/)
    .map(word => word.charAt(0).toUpperCase() + word.slice(1))
    .join('');
}

/**
 * Group keys by namespace prefix
 */
function groupKeysByPrefix(keys) {
  const groups = new Map();

  for (const key of keys) {
    const prefix = key.split('.')[0];
    if (!groups.has(prefix)) {
      groups.set(prefix, []);
    }
    groups.get(prefix).push(key);
  }

  return groups;
}

/**
 * Generate the type file
 */
function generateTypes() {
  const enDir = path.join(LOCALES_DIR, 'en');

  // Collect keys for each namespace
  const namespaceKeys = new Map();
  let allKeys = [];

  for (const ns of NAMESPACES) {
    const nsPath = path.join(enDir, `${ns}.json`);
    if (!fs.existsSync(nsPath)) {
      console.warn(`Warning: ${nsPath} is missing.`);
      continue;
    }

    const nsData = JSON.parse(fs.readFileSync(nsPath, 'utf-8'));
    const keys = Object.keys(nsData);
    namespaceKeys.set(ns, keys);
    allKeys = allKeys.concat(keys);
  }

  // Group by prefix for backward compatibility
  const prefixGroups = groupKeysByPrefix(allKeys);

  // Generate the type file
  let output = `/**
 * Generated i18n type definitions
 *
 * This file is auto-generated by scripts/generate-i18n-types.mjs.
 * Do not edit this file directly.
 *
 * Generated with: pnpm run generate:i18n-types
 * Generated at: ${new Date().toISOString()}
 * Total key count: ${allKeys.length}
 * Namespace count: ${NAMESPACES.length}
 */

`;

  // Namespace type
  output += `/**
 * Available namespaces
 *
 * Each namespace stays small enough for focused editing:
 * - common: shared UI (~99 keys)
 * - analytics: analytics dashboard (~132 keys)
 * - session: sessions/projects (~116 keys)
 * - settings: settings manager (~501 keys)
 * - tools: tooling (~69 keys)
 * - error: error messages (~37 keys)
 * - message: message viewer (~66 keys)
 * - renderers: renderer components (~255 keys)
 * - update: update flow (~65 keys)
 * - feedback: feedback (~32 keys)
 * - recentEdits: recent edits (~20 keys)
 */
export type I18nNamespace =
${NAMESPACES.map(ns => `  | '${ns}'`).join('\n')};

`;

  // Key types for each namespace
  for (const [ns, keys] of namespaceKeys) {
    const typeName = `${toPascalCase(ns)}Keys`;
    output += `/**
 * ${ns} translation keys (${keys.length} keys)
 * File: locales/{lang}/${ns}.json
 */
export type ${typeName} =
${keys.sort().map(k => `  | '${k}'`).join('\n')};

`;
  }

  // Union of all translation keys
  output += `/**
 * Union type of all translation keys
 */
export type TranslationKey =
${allKeys.sort().map(k => `  | '${k}'`).join('\n')};

`;

  // Prefix list for backward compatibility
  const prefixes = Array.from(prefixGroups.keys()).sort();
  output += `/**
 * Available prefixes (backward compatibility)
 */
export type TranslationPrefix =
${prefixes.map(p => `  | '${p}'`).join('\n')};
`;

  // Write the generated file
  fs.writeFileSync(OUTPUT_PATH, output, 'utf-8');
  console.log(`Generated type file: ${OUTPUT_PATH}`);
  console.log(`Total ${allKeys.length} keys, ${NAMESPACES.length} namespaces, ${prefixes.length} prefixes`);

  // Namespace statistics
  console.log('\n=== Namespace key counts ===');
  for (const [ns, keys] of namespaceKeys) {
    console.log(`  ${ns}: ${keys.length} keys`);
  }
}

// Run generation
generateTypes();
